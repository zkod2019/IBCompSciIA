    private int findPlayerIndex(String str){
        int index = -1;
        String pnumber = str.split(",")[2];
        Iterator<Player> iterator = players.iterator();
        while(iterator.hasNext()){
            Player p = iterator.next();
            if(p.pnumber.equals(pnumber)){
                index = players.indexOf(p);
            }
        }
        return index;
    }



