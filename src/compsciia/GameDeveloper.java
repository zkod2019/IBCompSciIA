    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
    list8.clear();
    list3.setBackground(Color.white);list2.setBackground(Color.white);
    scoreOne = 0;
    Iterator<Player> iteratorOne = teamOne.iterator();
    while(iteratorOne.hasNext()){
    Player w = iteratorOne.next();
    scoreOne += w.currentgoal;
    }
    scoreTwo = 0;
    Iterator<Player> iteratorTwo = teamTwo.iterator();
    while(iteratorTwo.hasNext()){
    Player q = iteratorTwo.next();
    scoreTwo += q.currentgoal;
    }
    list8.add(scoreOne + " : " + scoreTwo);
    if (scoreOne>scoreTwo){
        list2.setBackground(Color.green);
    }
    else if(scoreOne<scoreTwo){
        list3.setBackground(Color.green);
    }
    else if(scoreOne==scoreTwo){
        list3.setBackground(Color.yellow);
        list2.setBackground(Color.yellow);
    }
    else{
        list3.setBackground(Color.white);list2.setBackground(Color.white);
    }
    
    
    }//GEN-LAST:event_jButton7ActionPerformed
    
