    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        list2.removeAll(); teamOne.clear();   //seperate game , when new game start score in 0 
        list3.removeAll(); teamTwo.clear(); currentgoal=0; 
        list4.removeAll(); teamThree.clear();
        list5.removeAll(); teamFour.clear();
        list7.removeAll(); list12.removeAll(); 
        list3.setBackground(Color.white);list2.setBackground(Color.white);
        list4.setBackground(Color.white);list5.setBackground(Color.white);
     
        if(players.size()<=1){
        GameDeveloper.infoBox("You must have more than one player.", "!");
        }
        
        Iterator<Player> iter = players.iterator();
     while(iter.hasNext()){
         Player q = iter.next();
         q.currentgoal=0; 
     }
     
     if(!extraplayers.isEmpty()){
     Iterator<Player> iteratorexp = extraplayers.iterator();
     while(iteratorexp.hasNext()){
         Player p = iteratorexp.next();
         players.remove(p);
         players.add(0,p);   
     }
     extraplayers.clear();
     }
    
     if(extraplayers.isEmpty()){  
         if(!lastplayer.isEmpty()){
         Iterator<Player> iteratorlast = lastplayer.iterator();
         while(iteratorlast.hasNext()){
         Player p = iteratorlast.next();
         players.remove(p);
         players.add(0,p);   
     }
         }   
     lastplayer.clear();
     }
        
      //process of distributing equal amount of players
     Iterator<Player> iterator = players.iterator();
     while(iterator.hasNext()){
         if(iterator.hasNext()){teamOne.add(iterator.next()); }
         if(iterator.hasNext()){teamTwo.add(iterator.next());}
         if(iterator.hasNext()){teamThree.add(iterator.next());}
         if(iterator.hasNext()){teamFour.add(iterator.next());}  
     }
     if(players.size()%2!=0){ 
         if(teamOne.size()!=teamTwo.size()){
             extraplayers.add(teamOne.get(teamOne.size()-1));
             teamOne.remove(teamOne.size()-1);
         }
         if(teamThree.size()!=teamFour.size()){
             extraplayers.add(teamThree.get(teamThree.size()-1));
             teamThree.remove(teamThree.size()-1);
         }
     }
     
     Iterator<Player> teamOnePlayers =  teamOne.iterator();
      while(teamOnePlayers.hasNext()){
          Player p = teamOnePlayers.next();
          list2.add(p.name);
          gameCount(p);
      }
      Iterator<Player> teamTwoPlayers =  teamTwo.iterator();
      while(teamTwoPlayers.hasNext()){
          Player p = teamTwoPlayers.next();
          list3.add(p.name);
          gameCount(p);
      }
      Iterator<Player> teamThreePlayers =  teamThree.iterator();
      while(teamThreePlayers.hasNext()){
          Player p = teamThreePlayers.next();
          list4.add(p.name);
          gameCount(p);
      }
      Iterator<Player> teamFourPlayers =  teamFour.iterator();
      while(teamFourPlayers.hasNext()){
          Player p = teamFourPlayers.next();
          list5.add(p.name);
          gameCount(p);
          lastplayer.add(teamFour.get(teamFour.size()-1));
      }
      Iterator<Player> extraiterator = extraplayers.iterator();
      while(extraiterator.hasNext()){
          list7.add(extraiterator.next().name);
      }     
              
    }//GEN-LAST:event_jButton10ActionPerformed
    
