    public void bSort(){
        Player cur;
        if (players.size()>1){
         for (int i=0; i<players.size(); i++){
            for (int z=0; z < players.size()-i-1; z++) {
              if (players.get(z).compareTo(players.get(z+1)) > 0){
                cur = players.get(z);
                players.set(z,players.get(z+1) );
                players.set(z+1, cur);
                    }
                }
            }
        }   
    }
        
    
